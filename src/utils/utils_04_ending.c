#include "../include/cub3D.h"

void	end_game(t_game *game)
{
	if (check_if_end(game))
	{
		if (game->level == 2)
		{
			game->state = GAME_OVER;
			you_win();
			display_home_screen(game, &game->home_screen);
			return ;
		}
		free_game(game);
	}
	if (check_if_dead(game))
	{
		game_over();
		destroy_game(game);
		exit (0);
	}
}

